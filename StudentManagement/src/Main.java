    private void btnUploadImageActionPerformed(java.awt.event.ActionEvent evt) {                                               
        // TODO add your handling code here:
        JFileChooser chooser = new JFileChooser();
        FileNameExtensionFilter filter = new FileNameExtensionFilter(
                "JPG & GIF Images", "jpg", "gif");
        chooser.setFileFilter(filter);
        chooser.showOpenDialog(this);
        
        File sourceFile = chooser.getSelectedFile();
        if(sourceFile != null) {
            String sourceFilePath = sourceFile.getAbsolutePath();
            txtImagePath.setText(sourceFilePath);
            int lblImageHeight = lblImage.getHeight();
            int lblImageWidth = lblImage.getWidth();
            
            try {
                BufferedImage thumbnail;
                thumbnail = Thumbnails.of(sourceFile)
                                      .size(lblImageWidth, lblImageHeight)
                                      .asBufferedImage();
                
                ByteArrayOutputStream baos = new ByteArrayOutputStream();
                ImageIO.write(thumbnail,"jpg", baos);
                byteImage = baos.toByteArray();
                lblImage.setIcon(new ImageIcon(thumbnail));
            } catch(IOException e) {
                String msg = e.getMessage();
                JOptionPane.showMessageDialog(this, msg,
                        "Error!",JOptionPane.ERROR_MESSAGE);
            }
        }
        
    }                                              

