     private DefaultTableModel getStudentsTableModel(TableModel tm) {
        int nRow = tm.getRowCount();
        int nCol = tm.getColumnCount();
        
        Object[][] tableData = new Object[nRow][nCol];
        
        for(int i=0; i<nRow; i++) {
            for(int j=0; j<nCol-1; j++) {
                tableData[i][j] = tm.getValueAt(i, j);
            }
        }
        
        String colHeads[] = {"Enrollment No", "Name", "Gender", "Age", "Contact No", "DOB", "Qualification", 
            "Address"};
        
        DefaultTableModel tableModel = new DefaultTableModel(tableData, colHeads) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        
        return tableModel;
    }
    private void fillDataTable() {
        try {
            String sql = "SELECT "
                    + "enrollment_no, name, gender, age, contact_no, dob, qualification, address, image "
                    + "FROM students";
            ps = conn.prepareStatement(sql);
            rs = ps.executeQuery();
            jtableData.setModel(getStudentsTableModel(DbUtils.resultSetToTableModel(rs)));
            
        } catch(SQLException e) {
            String msg = e.getMessage();
            JOptionPane.showMessageDialog(this, msg,
                        "Error!",JOptionPane.ERROR_MESSAGE);
        }
    }
    
